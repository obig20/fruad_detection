import matplotlib.pyplot as plt
import seaborn as sns
def explatory_data_analysis(df,title='EDA'):
    print(f'Exploratory Data Analysis for {title}-summary statics')
    print(df.describe())
    print(f'Exploratory Data Analysis for {title}-null values')
    print(df.isnull().sum())
    print(f'Exploratory Data Analysis for {title}-data types')
    print(df.dtypes)
    print(f'Exploratory Data Analysis for {title}-shape')
    print(df.shape)
    print(f'Exploratory Data Analysis for {title}-columns')
    print(df.columns)
    print(f'Exploratory Data Analysis for {title}-head')
    print(df.head())
    print(f'Exploratory Data Analysis for {title}-tail')
    print(df.tail())
    print(f'Exploratory Data Analysis for {title}-info')
    print(df.info())
    print(f'Exploratory Data Analysis for {title}-correlation')
    print(df.corr())
    print(f'Exploratory Data Analysis for {title}-unique values')
    print(df.nunique())
    print(f'Exploratory Data Analysis for {title}-value counts')
    print(df.value_counts())
    print(f'Exploratory Data Analysis for {title}-duplicate values')
    print(df.duplicated().sum())
    print(f'Exploratory Data Analysis for {title}-heatmap')
    sns.heatmap(df.corr(),annot=True)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-pairplot')
    sns.pairplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-distplot')
    sns.distplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-boxplot')
    sns.boxplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-countplot')
    sns.countplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-barplot')
    sns.barplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-scatterplot')
    sns.scatterplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-lineplot')
    sns.lineplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-regplot')
    sns.regplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-lmplot')
    sns.lmplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-catplot')
    sns.catplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-violinplot')
    sns.violinplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-stripplot')
    sns.stripplot(df)
    plt.show()
    print(f'Exploratory Data Analysis for {title}-swarmplot')
    sns.swarmplot(df)
    plt.show()
    if 'amount' in df.columns:
        
        plt.figure(figsize=(10,5))
        sns.histplot(df['amount'],kde=True,bins=30)
    
        plt.xlabel('amount')
        plt.ylabel('frequency')
        print(f'Exploratory Data Analysis for {title}-histplot')
        plt.show()